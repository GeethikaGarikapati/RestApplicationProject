package com.demo.Model;

public class FileUploadResponse {

	private String fileName;

	private String FileUploadResponse;

	/**
	 * @return the fileUploadResponse
	 */
	public String getFileUploadResponse() {
		return FileUploadResponse;
	}

	/**
	 * @param fileUploadResponse
	 *            the fileUploadResponse to set
	 */
	public void setFileUploadResponse(String fileUploadResponse) {
		FileUploadResponse = fileUploadResponse;
	}

	/**
	 * @return the fileName
	 */
	public String getFileName() {
		return fileName;
	}

	/**
	 * @param fileName
	 *            the fileName to set
	 */
	public void setFileName(String fileName) {
		this.fileName = fileName;
	}

}
